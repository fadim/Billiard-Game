
public class ReadXMLFile {
    
    private int ballNo;
    private int radius;
    private int mass;
    private int numberOfPocket;
    private int width;
    private int height;
    private int frictionCoefficient;

    
    public int getBallNo() {
        return ballNo;
    }

    public void setBallNo(int ballNo) {
        this.ballNo = ballNo;
    }

    public int getMass() {
        return mass;
    }

    public void setMass(int mass) {
        this.mass = mass;
    }

    public int getRadius() {
        return radius;
    }

    public void setRadius(int radius) {
        this.radius = radius;
    }

    public int getFrictionCoefficient() {
        return frictionCoefficient;
    }

    public void setFrictionCoefficient(int frictionCoefficient) {
        this.frictionCoefficient = frictionCoefficient;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getNumberOfPocket() {
        return numberOfPocket;
    }

    public void setNumberOfPocket(int numberOfPocket) {
        this.numberOfPocket = numberOfPocket;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }
    
    
    
}
